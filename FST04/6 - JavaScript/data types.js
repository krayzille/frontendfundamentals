//Primitive Data Types

//Strings: text , can be used with '' or ""
let firstName = "John";
console.log("First Name:", firstName, "Special Characters: !,?,()");

console.log("The type of date is", typeof firstName);

//Numbers
let age = 30;
console.log("My age is ", age);
console.log("The type of data is ", typeof age); // outputs "number"

//Boolean: True r False
let student = true;
console.log("Are you a Student?", student);
console.log("The Typeof is a", typeof student);

//Null: Intentional absence of a value
let car = null;
console.log("Car:",car);
console.log("Typeof Car:", typeof car);
//Shows an output of "Object"

//Undefined: Unintentional absence of value
let city;
console.log(city); //shows undefined
console.log(typeof city);

